#include "libft.h"

int	ft_putunbr(unsigned int n)
{
	if (n >= 10)
		return (ft_putunbr(n / 10) + ft_putchar("0123456789"[n % 10]));
	else
		return (ft_putchar("0123456789"[n % 10]));
}
